
public class Variable01 {
	public static void main(String[] args) {
	//変数の宣言
		//基本構文「型 変数名;」	
		int num1;
		String str1;
	//初期化・・宣言した変数に初期値を代入すること
		//基本構文「型 変数名 = 代入するデータ;」	
		num1 = 10;
		str1 = "Hello World!";
		System.out.println(num1);
		System.out.println(str1);
		
		num1 = 20;
		System.out.println(num1);
		
		int num2 = 30;
		System.out.println(num2);
	}

}
